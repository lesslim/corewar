#include "libft.h"

int	ft_uchar_len(char const *c)
{
	if (!(*c & 0b10000000))
		return (1);
	else if ((*c & 0b11110000) == 0b11110000)
		return (4);
	else if ((*c & 0b11100000) == 0b11100000)
		return (3);
	else
		return (2);
}

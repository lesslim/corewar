#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < -9 || n > 9)
		ft_putnbr(n / 10);
	else if (n < 0)
		ft_putchar('-');
	ft_putchar(n < 0 ? (n % 10 * -1 + '0') : (n % 10 + '0'));
}

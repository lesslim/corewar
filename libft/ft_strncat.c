#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t	i;

	i = ft_strlen(dest);
	while (n > 0 && *src)
	{
		dest[i] = *src;
		i++;
		src++;
		n--;
	}
	dest[i] = '\0';
	return (dest);
}

#include "libft.h"

int	ft_isprime(int num)
{
	int	i;

	if (num == 1)
		return (0);
	i = 2;
	while (i * i <= num)
	{
		if (i < 0)
			return (0);
		if (num % i == 0)
			return (0);
		i++;
	}
	return (1);
}
